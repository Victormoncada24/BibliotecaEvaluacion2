class empleado():
    def __init__(self,id_empleado,nombre,fecha_contrato,salario,correo,telefono,direccion,rut,fecha_nacimiento):
        self.id_empleado = id_empleado
        self.nombre = nombre
        self.fecha_contrato = fecha_contrato
        self.salario = salario
        self.correo = correo
        self.telefono = telefono
        self.direccion = direccion
        self.rut = rut
        self.fecha_nacimiento = fecha_nacimiento